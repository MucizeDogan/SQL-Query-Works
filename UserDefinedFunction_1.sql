CREATE FUNCTION DBO.TOPLA(@NUMBER1 AS INT,@NUMBER2 AS INT)
RETURNS INT
AS
BEGIN
	DECLARE @RESULT AS INT 
	SET @RESULT=@NUMBER1+@NUMBER2
	RETURN @RESULT

END

